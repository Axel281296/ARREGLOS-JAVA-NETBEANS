    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
       if (btnNuevo.getText().equals("Nuevo")) {
            this.execLimpiaCampos();
            execHabilitaCampos(true);
            btnNuevo.setText("Grabar");
        } else {
            empeado regAlum = new empeado();
            
            regAlum.setCedula(txtCedula.getText());
            regAlum.setNombres(txtNombre.getText());
            regAlum.setApellidos(txtApellido.getText());
            regAlum.setTelefono(txtTelefono.getText());
            regAlum.setEdad(txtEdad.getText());
            regAlum.setCivil(cmbEstado.getSelectedItem().toString());
            regAlum.setSueldo(txtSueldo.getText());
            
            
            dataPersonas.add(regAlum);
           
            btnNuevo.setText("Nuevo");
            this.execLimpiaCampos();
            execConfiguraTabla();
            execHabilitaCampos(false);
        }
        
    }//GEN-LAST:event_btnNuevoActionPerformed

