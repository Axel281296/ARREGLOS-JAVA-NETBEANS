 private void execLimpiaCampos() {
        this.txtCedula.setText("");
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.txtTelefono.setText("");
        this.txtEdad.setText("");
        this.cmbEstado.setSelectedIndex(-1);
        this.txtSueldo.setText("");
    }
    
    private void execHabilitaCampos(boolean prmValor) {
        this.txtCedula.setEnabled(prmValor);
        this.txtNombre.setEnabled(prmValor);
        this.txtApellido.setEnabled(prmValor);
        this.txtTelefono.setEnabled(prmValor);
        this.txtEdad.setEnabled(prmValor);
        this.cmbEstado.setEnabled(prmValor);
        this.txtSueldo.setEnabled(prmValor);
    }
    
    private void execConfiguraTabla() {
        Object[] h = new Object[]{"Cedula","Nombres", "Apellidos","Edad","Telefono","Estado Civil","Sueldo"};
        DefaultTableModel tbl = new DefaultTableModel(h,0);
        
        tbl.setNumRows(0);
        for (empeado reg : this.dataPersonas) {
            tbl.addRow(new Object[]{reg.getCedula(), reg.getNombres(), reg.getApellidos(),reg.getEdad(),reg.getTelefono(),reg.getCivil(),reg.getSueldo()});
        }
        
        this.tblDatos.setModel(tbl);
        
        
    }
    
    

 

