    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed

        if(this.btnnuevo.getText().equals("NUEVO"))
        {
            clear();
            this.btnnuevo.setText("GRABAR");
             n=n+1; 
             this.txtid.setText(""+n);
        } 
       else
       {
       
      int m= n+1; 
       alumno reg= new alumno();
       reg.setId(m);
       reg.setCedula(this.txtcedula.getText());
       reg.setNombre(this.txtnombre.getText());
       reg.setApellido(this.txtapellidos.getText());
       reg.setMadre(this.txtmadre.getText());
       reg.setPadre(this.txtpadre.getText());
       reg.setEdad(Integer.parseInt(this.txtedad.getText()));
       reg.setSexo(jComboBox1.getSelectedItem().toString());
       reg.setCarrera(this.txtcarrera.getText());
       Date d= new Date(this.txtfchadenacimiento.getText());
       reg.setFechadenacimiento(d); 
       data.add(reg);
       n++;
       }
    }//GEN-LAST:event_btnnuevoActionPerformed

